	model := openai.GPT3Dot5Turbo
	if os.Getenv("OPENAI_MODEL") != "" {
		model = os.Getenv("OPENAI_MODEL")
	}

	log.Printf("–û—Ç–ø—Ä–∞–≤–∫–∞ –∑–∞–ø—Ä–æ—Å–∞ –∫ OpenAI (–º–æ–¥–µ–ª—å: %s, –¥–ª–∏–Ω–∞ –∑–∞–ø—Ä–æ—Å–∞: %d —Å–∏–º–≤–æ–ª–æ–≤)",
		model, len(prompt))

